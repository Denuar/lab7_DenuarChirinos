    private void btn_aceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_aceptarActionPerformed
        // TODO add your handling code here:
        for (int i = 0; i < usuarios.size(); i++) {
            if (usuarios.get(i).getUsuario().equals(txt_logUser.getText()) && usuarios.get(i).getContraseña().equals(txt_logPass.getText())){
                JOptionPane.showMessageDialog(this, "Bienvenido " + usuarios.get(i).getNombre());
                estadoLogU=1;
                userLogeado=i;
                jMenu3.setEnabled(true);
                jd_logUser.setVisible(false);
            }
        }
        if(estadoLogU==0){
            JOptionPane.showMessageDialog(this, "Usuario o Contraseña Incorrecto");
        }
        
        //Reinicio de variables
        txt_logUser.setText("");
        txt_logPass.setText("");
    }//GEN-LAST:event_btn_aceptarActionPerformed

