    private void btn_guardarCancionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_guardarCancionActionPerformed
        // TODO add your handling code here:
        
        nombreC = txt_nCancion.getText();
        nombreA = txt_nArtista.getText();
        fecha=dc1.getDatoFecha();
        duracion = Integer.parseInt(txt_duracion.getText());
        cancion = new Cancion(nombreC, nombreA, fecha, duracion);
        artistas.get(artistLogeado).setAlbum(new Album("Sencillo", nombreA, fecha));//agrego la cancion al artista que se encuentra autenticado
        
        canciones.add(cancion);
        DefaultComboBoxModel modelo = (DefaultComboBoxModel) cb_todasCanciones.getModel();
        modelo.addElement(cancion);
        cb_todasCanciones.setModel(modelo);
        //reinicio las variables
        txt_nCancion.setText("");
        txt_nArtista.setText("");
        txt_duracion.setText("");
    }//GEN-LAST:event_btn_guardarCancionActionPerformed

